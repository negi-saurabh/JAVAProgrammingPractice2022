package tuf.pattern;

public class Pattern4 {
    public static void main(String[] args) {
        int n = 5;
        for(int i=1 ; i<n+1 ; i++){
            for(int j=0 ; j < i  ; j++){
                System.out.print(i);
                System.out.print(" ");
            }
            System.out.println();
        }
    }
}
